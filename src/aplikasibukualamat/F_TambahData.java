    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
           int selectedRow = Table.getSelectedRow();

    
        // Mendapatkan data dari baris yang dipilih
        String id = model.getValueAt(selectedRow, 0).toString();
        String Nama = tfNama.getText();
        String Warga = tfWarga.getText();
        String jalan = tfJalan.getText();
        String prov = cbProv.getSelectedItem().toString();
        String kota = tfKota.getText();
        String rt = tfRT.getText();
        String telp = tfTelp.getText();

        // Mengubah data pada baris yang dipilih
        model.setValueAt(Nama, selectedRow, 0);
        model.setValueAt(Warga, selectedRow, 1);
        model.setValueAt(jalan, selectedRow, 2);
        model.setValueAt(prov, selectedRow, 3);
        model.setValueAt(kota, selectedRow, 4);
        model.setValueAt(rt, selectedRow, 5);
        model.setValueAt(telp, selectedRow, 6);

        btnClearActionPerformed(evt); // Mengosongkan kolom input setelah pengubahan
        model.fireTableDataChanged();
    
    
    }//GEN-LAST:event_btnUbahActionPerformed

