    private void initializeTableModel() {
        model = new DefaultTableModel();
        // Atur kolom tabel jika diperlukan
        model.addColumn("Nama");
        model.addColumn("Warga");
        model.addColumn("Jalan");
        model.addColumn("Provinsi");
        model.addColumn("Kota");
        model.addColumn("RT");
        model.addColumn("Telepon");

        // Atur model pada tabel
        Table.setModel(model);
    }
    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        String Nama = tfNama.getText();
        String Warga = tfWarga.getText();
        String jalan = tfJalan.getText();
        String prov = cbProv.getSelectedItem().toString();
        String kota = tfKota.getText();
        String rt = tfRT.getText();
        String telp = tfTelp.getText();

        // Menambahkan baris baru ke tabel
        model.addRow(new Object[]{Nama, Warga, jalan, prov, kota, rt, telp});
        model.fireTableDataChanged();

        
        
    }//GEN-LAST:event_btnSimpanActionPerformed

    
