    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        
        // Mendeteksi double-click
        JTable target = (JTable) evt.getSource();
        int row = target.getSelectedRow();

        // Mengambil data dari baris yang diklik
        String Nama = model.getValueAt(row, 0).toString();
        String Warga = model.getValueAt(row, 1).toString();
        String jalan = model.getValueAt(row, 2).toString();
        String prov = model.getValueAt(row, 3).toString();
        String kota = model.getValueAt(row, 4).toString();
        String rt = model.getValueAt(row, 5).toString();
        String telp = model.getValueAt(row, 6).toString();

        // Menetapkan data ke JTextField di atas
        tfNama.setText(Nama);
        tfWarga.setText(Warga);
        tfJalan.setText(jalan);
        cbProv.setSelectedItem(prov); // Menggunakan setSelectedItem untuk JComboBox
        tfKota.setText(kota);
        tfRT.setText(rt);
        tfTelp.setText(telp);
    
    }//GEN-LAST:event_TableMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
     DefaultTableModel model = (DefaultTableModel) Table.getModel();

    // Mendapatkan indeks baris yang dipilih
    int selectedRow = Table.getSelectedRow();

    // Memeriksa apakah ada baris yang dipilih sebelum menghapus
    if (selectedRow != -1) {
        // Hapus baris yang dipilih dari model
        model.removeRow(selectedRow);
        btnClearActionPerformed(evt); // Mengosongkan kolom input setelah pengubahan
    } else {
        // Tampilkan pesan bahwa tidak ada baris yang dipilih untuk dihapus
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btnHapusActionPerformed

